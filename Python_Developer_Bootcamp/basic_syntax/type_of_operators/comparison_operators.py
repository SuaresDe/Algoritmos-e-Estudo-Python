#equality
cash_balance = 450
cash_withdrawal = 200
print(cash_balance == cash_withdrawal)
#False

#difference
cash_balance = 450
cash_withdrawal = 200
print(cash_balance != cash_withdrawal)
#True

#greater than/greater than or equal
cash_balance = 450
cash_withdrawal = 200
print(cash_balance > cash_withdrawal)  #greater
#True
print(cash_balance >= cash_withdrawal) #equal
#True

#less than / less than or equal
cash_balance = 450
cash_withdrawal = 200
print(cash_balance < cash_withdrawal)  #less
#False
print(cash_balance <= cash_withdrawal) #equal
#False